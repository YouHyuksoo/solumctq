/** 재귀적 번역 구조 타입 - 문자열 리터럴 대신 string/string[] 사용 */
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends readonly string[]
    ? readonly string[]
    : T[K] extends string
      ? string
      : T[K] extends object
        ? DeepStringify<T[K]>
        : T[K];
};

